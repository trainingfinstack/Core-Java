package operators;

public class LeftShift {
	
	public static void main(String args[]) 
	
	{
		
		System.out.println(10 << 2); // 10*2^2=10*4=40
		
		System.out.println(10 << 4); // 10*2^4=10*16=160
		
		System.out.println(10 << 2); // 10*2^2=10*4=40
		
		System.out.println(10 << 4); // 10*2^4=10*16=160
		
		
		
	}
	
	

}
